numbers=[1,2,3,4,5,6]
#append methodu listenin sonuna ekleyebilmemizi sağlar
numbers.append(7)

#insert index ile istediğin yere ekleyebilirsin
numbers.insert(3,8)

#pop methodu sondan bir eleman silme işine yarar.parantez içine index numarası yazarsan o indexteki değeri siler.
numbers.pop()  

#remove methodu listede o değeri bulup siler.Bu örnekte 1 değerini bulup siler
numbers.remove(1)  

#sort methodu sıralar
numbers.sort()

#reverse methodu listenin tam tersine çevrilmesini sağlar.
numbers.reverse()
#clear methodu listeyi temizler
numbers.clear()

